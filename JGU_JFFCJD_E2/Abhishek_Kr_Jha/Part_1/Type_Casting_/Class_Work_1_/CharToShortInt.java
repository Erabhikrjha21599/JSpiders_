package Abhishek_Kr_Jha.Part_1.Type_Casting_.Class_Work_1_;

public class CharToShortInt {
    public static void main(String[] args) {
        char c = 'A';
        short s = (short) c;
        int i = (int) c;

        System.out.println(c);
        System.out.println(s);
        System.out.println(i);
    }
    
}
