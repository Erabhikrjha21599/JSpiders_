public class CalculateTotalPens {
    public static void main(String[] args) {
        int ram_pens = 4;
        int hari_pens = 7;
        int raghu_pens = 9;
        int shyam_pens = 11;
        int total_pens = ram_pens+hari_pens+raghu_pens+shyam_pens;

        System.out.println(ram_pens);
        System.out.println(hari_pens);
        System.out.println(raghu_pens);
        System.out.println(shyam_pens);
        System.out.println(total_pens);
    }
    
}
