public class CheckCharIsUpperVowelOrNot {
    public static void main(String[] args) {
        char c1 = 'E';
        if (c1 == 'A' || c1 == 'E' || c1 == 'I' || c1 == 'O' || c1 == 'U') {
            System.out.println("char is upper case vowel");
            
        } else {
            System.out.println("char is not upper case vowel");
            
        }
    }
}
