public class PrintEquivalentMonthName {
    public static void main(String[] args) {
        int i1 = 1;

        if (i1 == 1) {
            System.out.println("january");

        } else if (i1 == 2) {
            System.out.println("february");

        } else if (i1 == 12) {
            System.out.println("december");

        } else {
            System.out.println("invalid");

        }
    }
}
